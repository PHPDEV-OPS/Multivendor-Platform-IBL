        document.addEventListener('DOMContentLoaded', function() {
            // User profile dropdown functionality
            const userProfileDropdown = document.querySelector('.user-profile-dropdown');
            const userDropdownMenu = document.querySelector('.user-dropdown-menu');
            
            if (userProfileDropdown && userDropdownMenu) {
                // Toggle dropdown on click
                userProfileDropdown.addEventListener('click', function(e) {
                    e.stopPropagation();
                    const isVisible = userDropdownMenu.style.display === 'block';
                    userDropdownMenu.style.display = isVisible ? 'none' : 'block';
                });
                
                // Close dropdown when clicking outside
                document.addEventListener('click', function(e) {
                    if (!userProfileDropdown.contains(e.target) && !userDropdownMenu.contains(e.target)) {
                        userDropdownMenu.style.display = 'none';
                    }
                });
                
                // Close dropdown on escape key
                document.addEventListener('keydown', function(e) {
                    if (e.key === 'Escape') {
                        userDropdownMenu.style.display = 'none';
                    }
                });
            }

            // Initialize wishlist and compare counts
            function loadCounts() {
                // Load wishlist count
                fetch('{{ route("wishlist.count") }}')
                    .then(response => response.json())
                    .then(data => {
                        const wishlistCountElements = document.querySelectorAll('.wishlist_count');
                        wishlistCountElements.forEach(element => {
                            element.textContent = data.totalItems || 0;
                        });
                    })
                    .catch(error => {
                        console.error('Error loading wishlist count:', error);
                    });

                // Load compare count
                fetch('{{ route("compare.count") }}')
                    .then(response => response.json())
                    .then(data => {
                        const compareCountElements = document.querySelectorAll('.compare_count');
                        compareCountElements.forEach(element => {
                            element.textContent = data.totalItems || 0;
                        });
                    })
                    .catch(error => {
                        console.error('Error loading compare count:', error);
                    });
            }

            // Load counts on page load
            loadCounts();
        });
    </script>
